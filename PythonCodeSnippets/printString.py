# Print with string literal
print( "Hello World" )

# Print using a string variable
thingToPrint = "Hello World"
print( thingToPrint )

# Print with concatenation
print( "Hello" + " World" )

# Print multiple string literals
print( "Hello", "World" )

# Print mixing string literals and variables
secondPart = "World"
print( "Hello", secondPart )
